var shortestBridge = function (grid) {
  const dy = [0, 1, 0, -1];
  const dx = [1, 0, -1, 0];
  const island1 = {},
    island2 = {};

  function getIsland(island, y, x) {
    if (grid[y] === undefined || grid[y][x] === undefined || grid[y][x] === 0) return;
    island[`${y} ${x}`] = true;
    grid[y][x] = 0;
    for (let d = 0; d < 4; d++) {
      getIsland(island, y + dy[d], x + dx[d]);
    }
  }
  
  for (let y = 0; y < grid.length; y++) {
    let flag = false;
    for (let x = 0; x < grid[0].length; x++) {
      if (grid[y][x] === 1) {
        if (Object.keys(island1).length === 0) getIsland(island1, y, x);
        else {
          getIsland(island2, y, x);
          flag = true;
          break;
        }
      }
    }
    if (flag) break;
  }

  let minFlipCount = grid.length * 2;
  for (let coord1 of Object.keys(island1)) {
    for (let coord2 of Object.keys(island2)) {
      const [x1, y1] = coord1.split(' ');
      const [x2, y2] = coord2.split(' ');
      const distance = Math.abs(x1 - x2) + Math.abs(y1 - y2);
      minFlipCount = Math.min(minFlipCount, distance - 1);
    }
  }

  return minFlipCount;
};

