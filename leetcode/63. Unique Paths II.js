/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
var uniquePathsWithObstacles = function (grid) {
  const m = grid.length,
    n = grid[0].length;
  if (m === 1 && n === 1 && grid[m - 1][n - 1] === 1) return 0;

  for (y = 0; y < m; y++) {
    for (x = 0; x < n; x++) {
      if (grid[y][x] === 1) continue;
      const top = y - 1 >= 0 && grid[y - 1][x] !== 1 ? grid[y - 1][x] : 0;
      const left = x - 1 >= 0 && grid[y][x - 1] !== 1 ? grid[y][x - 1] : 0;
      grid[y][x] = top + left;
      if (y === 0 && x === 0) grid[y][x] = -1;
    }
  }

  return grid[m - 1][n - 1] === 1 || grid[m - 1][n - 1] === 0 ? 0 : -grid[m - 1][n - 1];
};

